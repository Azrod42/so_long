#include"libft.h"

char	*ft_strrchr_ptf(const char *s, int c)
{
	int	i;

	i = 0;
	while (c >= 256)
		c -= 256;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			return (&((char *)s)[i]);
		i--;
	}
	return (NULL);
}
